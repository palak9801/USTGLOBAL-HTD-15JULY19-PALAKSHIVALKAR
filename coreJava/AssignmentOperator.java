public class AssignmentOperator{
 public static void main(String []args){
    int i=20, j=10;
	System.out.println("i="+i);
	System.out.println("j="+j);
	
	i += j;
	System.out.println("i+j="+i);
	
	i-=j;
		System.out.println("i-j="+i);
		
		i*=j;
		System.out.println("i*j="+i);
		
		i/=j;
		System.out.println("i/j="+i);
		
		i%=j;
		System.out.println("i%j="+i);
		
		
	
	}
	}
	