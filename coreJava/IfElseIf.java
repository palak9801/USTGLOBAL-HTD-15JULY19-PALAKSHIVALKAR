public class IfElseIf{
	public static void main(String []args){
		
		int a=100, b=80, c=30;
		
		if(a>b)
		{
			System.out.println("a is greater");
		}
		else if(b>c)
			{
				System.out.println("b is greater");
			}
			else{
				System.out.println("c is greater");
			}
		
	}
}

			